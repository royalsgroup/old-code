<script type="text/javascript">
         
    function get_class_modal(class_id){
         
        $('.fn_class_data').html('<p style="padding: 20px;"><p style="padding: 20px;text-align:center;"><img src="<?php echo IMG_URL; ?>loading.gif" /></p>');
        $.ajax({       
          type   : "POST",
          url    : "<?php echo site_url('classes/get_single_class'); ?>",
          data   : {class_id : class_id},  
          success: function(response){                                                   
             if(response)
             {
                $('.fn_class_data').html(response);
             }
          }
       });
    }
</script>
